#include "formulas.h"

float addition(float num1, float num2)
{
  return num1 + num2; //Addition calculation
}

float subtraction(float num1, float num2)
{
  return num1 - num2;
}

float multiplication(float num1, float num2)
{
return num1 * num2;
}

float division(float num1, float num2)
{
  return num1 / num2;
}
